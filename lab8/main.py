import math
import cmath
import numpy as np
import matplotlib.pyplot as plt

# =========================================================
# НЕЛІНІЙНЕ РІВНЯННЯ
# f(x) = x^3 - x - 2
# =========================================================

EPS = 1e-6
MAX_ITER = 100

# ---------------------------------------------------------
# Функція та похідні
# ---------------------------------------------------------

def f(x):
    return x**3 - x - 2

def df(x):
    return 3 * x**2 - 1

def d2f(x):
    return 6 * x


# =========================================================
# 1. ТАБУЛЯЦІЯ
# =========================================================

def tabulation(a, b, h):
    x = a

    file = open("tabulation.txt", "w")

    print("Табуляція функції:")
    print("----------------------------")

    while x <= b:
        y = f(x)

        print(f"x = {x:.2f}   f(x) = {y:.6f}")
        file.write(f"{x:.6f} {y:.6f}\n")

        x += h

    file.close()


# =========================================================
# 2. МЕТОД ПРОСТОЇ ІТЕРАЦІЇ
# x = cubic_root(x + 2)
# =========================================================

def phi(x):
    return (x + 2) ** (1 / 3)

def simple_iteration(x0):
    print("\nМетод простої ітерації")

    for i in range(MAX_ITER):

        x1 = phi(x0)

        print(f"Ітерація {i+1}: x = {x1}")

        if abs(x1 - x0) < EPS and abs(f(x1)) < EPS:
            return x1, i + 1

        x0 = x1

    return x0, MAX_ITER


# =========================================================
# 3. МЕТОД НЬЮТОНА
# =========================================================

def newton(x0):
    print("\nМетод Ньютона")

    for i in range(MAX_ITER):

        x1 = x0 - f(x0) / df(x0)

        print(f"Ітерація {i+1}: x = {x1}")

        if abs(x1 - x0) < EPS and abs(f(x1)) < EPS:
            return x1, i + 1

        x0 = x1

    return x0, MAX_ITER


# =========================================================
# 4. МЕТОД ХОРД
# =========================================================

def chord(x0, x1):
    print("\nМетод хорд")

    for i in range(MAX_ITER):

        x2 = x1 - f(x1) * (x1 - x0) / (f(x1) - f(x0))

        print(f"Ітерація {i+1}: x = {x2}")

        if abs(x2 - x1) < EPS and abs(f(x2)) < EPS:
            return x2, i + 1

        x0 = x1
        x1 = x2

    return x2, MAX_ITER


# =========================================================
# 5. МЕТОД ЧЕБИШЕВА
# =========================================================

def chebyshev(x0):
    print("\nМетод Чебишева")

    for i in range(MAX_ITER):

        fx = f(x0)
        dfx = df(x0)
        d2fx = d2f(x0)

        x1 = x0 - fx / dfx - (fx**2 * d2fx) / (2 * dfx**3)

        print(f"Ітерація {i+1}: x = {x1}")

        if abs(x1 - x0) < EPS and abs(f(x1)) < EPS:
            return x1, i + 1

        x0 = x1

    return x0, MAX_ITER


# =========================================================
# 6. ГРАФІК
# =========================================================

def draw_graph():

    x = np.linspace(-3, 3, 400)
    y = [f(i) for i in x]

    plt.figure(figsize=(8, 5))

    plt.plot(x, y)
    plt.axhline(0)
    plt.grid(True)

    plt.title("Графік функції")
    plt.xlabel("x")
    plt.ylabel("f(x)")

    plt.show()


# =========================================================
# 7. СХЕМА ГОРНЕРА
# =========================================================

# P(x) = x^3 - 6x^2 + 11x - 6

coeffs = [1, -6, 11, -6]

def horner(coeffs, x):

    result = coeffs[0]

    for i in range(1, len(coeffs)):
        result = result * x + coeffs[i]

    return result


# =========================================================
# 8. ПОХІДНА МНОГОЧЛЕНА
# =========================================================

def derivative_coeffs(coeffs):

    n = len(coeffs) - 1

    deriv = []

    for i in range(n):
        deriv.append(coeffs[i] * (n - i))

    return deriv


# =========================================================
# 9. НЬЮТОН ДЛЯ МНОГОЧЛЕНА
# =========================================================

def newton_polynomial(coeffs, x0):

    deriv = derivative_coeffs(coeffs)

    print("\nНьютон + Горнер")

    for i in range(MAX_ITER):

        fx = horner(coeffs, x0)
        dfx = horner(deriv, x0)

        x1 = x0 - fx / dfx

        print(f"Ітерація {i+1}: x = {x1}")

        if abs(x1 - x0) < EPS and abs(fx) < EPS:
            return x1, i + 1

        x0 = x1

    return x0, MAX_ITER


# =========================================================
# 10. КОМПЛЕКСНІ КОРЕНІ
# =========================================================

def complex_roots(coeffs):

    roots = np.roots(coeffs)

    print("\nКомплексні корені:")

    for r in roots:
        print(r)

# =========================================================
# СХЕМА ГОРНЕРА ДЛЯ МЕТОДУ ЛІНА
# =========================================================

def horner_lina(coeffs, p, q):

    n = len(coeffs) - 1

    b = [0] * (n + 1)

    # перші коефіцієнти
    b[n] = coeffs[n]
    b[n - 1] = coeffs[n - 1] + p * b[n]

    # рекурентна формула
    for i in range(n - 2, -1, -1):
        b[i] = coeffs[i] + p * b[i + 1] + q * b[i + 2]

    return b


# =========================================================
# МЕТОД ЛІНА
# =========================================================

def lina_method(coeffs, p0, q0):

    n = len(coeffs) - 1

    p = p0
    q = q0

    print("Метод Ліна")
    print("-----------------------------------")

    for iteration in range(MAX_ITER):

        # -----------------------------------------
        # Обчислення коефіцієнтів b
        # -----------------------------------------

        b = horner_lina(coeffs, p, q)

        # залишки
        r1 = b[0]
        r2 = b[1]

        print(f"\nІтерація {iteration + 1}")
        print(f"p = {p}")
        print(f"q = {q}")
        print(f"r1 = {r1}")
        print(f"r2 = {r2}")

        # -----------------------------------------
        # Перевірка точності
        # -----------------------------------------

        if abs(r1) < EPS and abs(r2) < EPS:
            break

        # -----------------------------------------
        # Побудова системи
        # -----------------------------------------

        c = horner_lina(b, p, q)

        A11 = c[2]
        A12 = c[3]

        A21 = c[1]
        A22 = c[2]

        # права частина
        B1 = -r2
        B2 = -r1

        # визначник
        det = A11 * A22 - A12 * A21

        if abs(det) < 1e-12:
            print("Визначник близький до нуля")
            return None

        # -----------------------------------------
        # Поправки
        # -----------------------------------------

        dp = (B1 * A22 - B2 * A12) / det
        dq = (A11 * B2 - A21 * B1) / det

        # -----------------------------------------
        # Оновлення
        # -----------------------------------------

        p += dp
        q += dq

        print(f"dp = {dp}")
        print(f"dq = {dq}")

    # =====================================================
    # ЗНАХОДЖЕННЯ КОМПЛЕКСНИХ КОРЕНІВ
    # =====================================================

    print("\n-----------------------------------")
    print("Квадратний множник:")
    print(f"x^2 + ({p})x + ({q})")

    D = p**2 - 4*q

    x1 = (-p + cmath.sqrt(D)) / 2
    x2 = (-p - cmath.sqrt(D)) / 2

    print("\nКомплексні корені:")

    print(f"x1 = {x1}")
    print(f"x2 = {x2}")

    return x1, x2

